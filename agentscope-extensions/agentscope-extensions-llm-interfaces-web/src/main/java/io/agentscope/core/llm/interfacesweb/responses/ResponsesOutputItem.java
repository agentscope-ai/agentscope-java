package io.agentscope.core.llm.interfacesweb.responses;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;
import java.util.List;

/** Output item in the OpenAI Responses format. */
@JsonInclude(JsonInclude.Include.NON_NULL)
public class ResponsesOutputItem {

    private String id;
    private String type;
    private String status;
    private String role;
    private List<ResponsesOutputContent> content;
    private String name;
    private String arguments;

    @JsonProperty("call_id")
    private String callId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public List<ResponsesOutputContent> getContent() {
        return content;
    }

    public void setContent(List<ResponsesOutputContent> content) {
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getArguments() {
        return arguments;
    }

    public void setArguments(String arguments) {
        this.arguments = arguments;
    }

    public String getCallId() {
        return callId;
    }

    public void setCallId(String callId) {
        this.callId = callId;
    }
}
